function normalizeCountry(raw) {
  if (!raw) return null;
  const c = COUNTRY_ALIASES[raw] || raw;
  return c;
}

function isFriendlyCountry(country) {
  const c = normalizeCountry(country);
  if (!c) return false;
  return BIDET_FRIENDLY_COUNTRIES.has(c);
}

function isFriendlyAtlyUrl(url) {
  const path = url.replace('https://www.atly.com/', '').toLowerCase();
  return FRIENDLY_URL_SLUGS.some((slug) => path.includes(`/${slug}/`) || path.startsWith(`${slug}/`) || path.includes(`-${slug}-`) || path.includes(`-${slug}/`));
}

